package sistemarh;


abstract class Pessoa {
    private int codigo;
    private int idade;
    String nome;

    public Pessoa (int cod,String name,int age){
        this.setCodigo(cod);
        this.setNome(name);
        this.setIdade(age);
    }
    public int getCodigo() {
        return codigo;
    }

    public void setCodigo(int codigo) {
        this.codigo = codigo;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public int getIdade() {
        return idade;
    }

    public void setIdade(int idade) {
        this.idade = idade;
    }

}
